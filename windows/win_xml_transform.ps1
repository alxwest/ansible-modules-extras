Set-Attr $result "changed" $false;

$xmlpath = Get-Attr -obj $params -name xmlpath -failifempty $true -emptyattributefailmessage "missing required argument: xmlpath"
$xdtpath = Get-Attr -obj $params -name xdtpath -failifempty $true -emptyattributefailmessage "missing required argument: xdtpath"
$destpath = Get-Attr -obj $params -name destpath -default $xmlpath

#Taken from http://stackoverflow.com/questions/8989737/web-config-transforms-outside-of-microsoft-msbuild
function XmlDocTransform($xml, $xdt, $dest)
{
    if (!$xml -or !(Test-Path -path $xml -PathType Leaf)) {
	    Set-Attr $result "nuget_error_xml" $xml
		Set-Attr $result "nuget_error_xdt" $xdt
        throw "File not found. $xml";
    }
    if (!$xdt -or !(Test-Path -path $xdt -PathType Leaf)) {
		Set-Attr $result "nuget_error_xml" $xml
		Set-Attr $result "nuget_error_xdt" $xdt
        throw "File not found. $xdt";
    }

    $xmldoc = New-Object Microsoft.Web.XmlTransform.XmlTransformableDocument;
    $xmldoc.PreserveWhitespace = $true
    $xmldoc.Load($xml);

    $transf = New-Object Microsoft.Web.XmlTransform.XmlTransformation($xdt);
    if ($transf.Apply($xmldoc) -eq $false)
    {
	    Set-Attr $result "nuget_error_xml" $xml
		Set-Attr $result "nuget_error_xdt" $xdt
        throw "Transformation failed."
    }
    $xmldoc.Save($destpath)
	$result.changed = $true
}

Try
{
    XmlDocTransform -xml $xmlpath -xdt $xdtpath -dest $destpath 
    Exit-Json $result;
}
Catch
{
     Fail-Json $result $_.Exception.Message
}
Finally
{
#Clean up
    Remove-Item $env:temp\Microsoft.Web.XmlTransform.dll
}

